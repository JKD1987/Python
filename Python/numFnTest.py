#Testing Number Functions

import math
print("Absolute function ", abs(-59))
print("Floating Absolute ", (math.fabs(-5)))
print("Ceiling value = ", math.ceil(12.001))
print("Floor value = ", math.floor(12.001))
print("Power 4 of 3 = ", math.pow(3,4))
print("Power 4 of 3 = ", 3**4)
print(round(123.5678))
print("Rounded Value to decimal places = ", round(35.53672,2))
print("Rounded Value to 1 integer place = ", round(35.53672,-1))
print("Truncate = ", math.trunc(123.23455))
print("Square root = ", round(math.sqrt(5), 2))
